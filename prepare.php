 <strong class="strong">WHAT DIET SHOULD BE GIVEN TO A PATIENT HAVING MALARIA? </strong>
 <p class="p">APatients of malaria tend to become weak very quickly. So, it is important that they eat food as often as they can. They may eat any food that they are able to eat  there are no diet restrictions. Patients with high fever may not feel hungry at all, so it is important to bring down fever in such cases, and make them feel more comfortable and hungry</p>
 <strong class="strong">HOW TO BRING DOWN FEVER </strong>
 <p class="p">1. Use of paracetamol </p>
 <p class="p">You will be given paracetamol tablets as part of your kit. When given in adequate doses, paracetamol can bring down fever from any cause within half an hour. However, paracetamol does not cure the disease that is causing the fever. So, its effect does not last long. The fever remains low for about 4-6 hours, and then the fever can rise again</p>
 <p class="p">Paracetamol can be safely given at any age and even during pregnancy, in the dose shown in the dosage chart. In this dose, it can be given 3-4 times a day if needed. If the fever is not very high, and the patient is able to tolerate the fever, there is no need to give paracetamol.  </p>
<p class="p">2. Use of plain water sponging </p>
<p class="p">When a patient has high fever, the whole body becomes hot, from inside and outside. One simple way of reducing the heat in the body is to apply water to the body. This is just like pouring water on a hot vessel to make it cool down. With a towel, plain water can be applied to the whole body, including the arms and legs, the chest and abdomen, front and back. This is the fastest way to bring down fever. It is not useful to just apply a wet towel on the forehead alone  when the whole body is hot, you must sponge the whole body to quickly bring down the fever.</p>
<p class="p">This method is particularly useful when the fever is very high and paracetamol may take long to bring it down. In such cases, first give a dose of paracetamol as shown in the dosage chart, and then start sponging. By the time the sponging has brought down the fever, the paracetamol will also take effect.</p>

