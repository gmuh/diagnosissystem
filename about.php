			<h2 class="green">Things to know about Malaria</h2>		 
<p>Malaria is a very common disease and a major health problem in the world today, mostly in Africa. Malaria kills twice as many people as previously thought; research has shown that malaria kills about one million, two hundred thousand (1.2 million) people every year which is nearly double the six hundred and fifty-five thousand (655,000) people estimated in 2009 (Guardian, 2012).  </p>			
<p> Malaria is characterized by a fever which is caused by a parasite that is spread by malaria bearing anopheles mosquito, which has become resistant to certain treatments and many insecticides (Wikipedia, 2012). </p>
<br><strong class="strong">What are common signs and symptoms of malaria?</strong></br
<p>The common signs and symptoms of malaria consist of high fever, headache, chills and shivers, nausea and vomiting. In severe form of the disease the signs and symptoms included severe vomiting and diarrhoea, generalized convulsion, delirium and impaired consciousness, followed by persistent coma and may be death.</p>
<br><strong class="strong">Which parts of the body do the malaria parasites usually infect? </strong></br>
<p>Malaria parasites are injected into the skin capillaries (small blood vessels) by mosquito bites. From there the parasites travel via the bloodstream to the liver. Here in the liver the parasites develop and multiply before entering the bloodstream again. In the bloodstream the parasites invade the red blood cells where further reproduction occurs. From there other internal organs like brain; kidney etc. can be affected as clumps of heavily infected red blood cells start 
blocking small blood vessels (capillary) obstructing the blood flow thereby causing interruption in the normal functions of the organs affected.</p>
<p>Malaria is preventable. Following measures can be taken to prevent malaria:
<li>Cover doors and windows with wire mesh/screens</li>
<li>Use mosquito nets (preferably insecticide treated). </li>
<li>Destroy mosquito breeding places around the habitat that reduces the number of mosquitoes.</li>
<li>Wear shirts with long sleeves/long dresses and full trousers after sunset</li>
<li>If symptoms are suggestive of malaria, seek immediate medical help.</li>
</p>

